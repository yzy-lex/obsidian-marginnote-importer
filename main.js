var MarginNoteImporterPlugin = class extends import_obsidian3.Plugin {
  constructor() {
    super(...arguments);
    this.settings = DEFAULT_SETTINGS;
  }
  async onload() {
    await this.loadSettings();
    this.addCommand({
      id: "import-mindmap",
      name: "\u4ECE MarginNote \u5BFC\u5165\u8111\u56FE",
      callback: () => this.runImport()
    });
    this.addRibbonIcon("brain", "MarginNote \u5BFC\u5165", () => this.runImport());
    this.addSettingTab(new ImporterSettingTab(this.app, this));
  }
  async loadSettings() {
    const data = await this.loadData();
    this.settings = Object.assign({}, DEFAULT_SETTINGS, data);
  }
  async saveSettings() {
    await this.saveData(this.settings);
  }
  /** Get the absolute path to this plugin's directory */
  getPluginDir() {
    const vaultPath = this.app.vault.adapter.getBasePath();
    return `${vaultPath}/.obsidian/plugins/${this.manifest.id}`;
  }
  /** Main import flow */
  async runImport() {
    const notice = new import_obsidian3.Notice("\u6B63\u5728\u8FDE\u63A5 MarginNote \u6570\u636E\u5E93...", 0);
    try {
      const pluginDir = this.getPluginDir();
      const db = await openDatabase(this.settings.dbPath, pluginDir);
      const topics = queryTopics(db);
      if (topics.length === 0) {
        notice.hide();
        new import_obsidian3.Notice("\u672A\u627E\u5230\u4EFB\u4F55\u5B66\u4E60\u96C6");
        db.close();
        return;
      }
      notice.hide();
      new TopicModal(this.app, topics, async (topic) => {
        const importNotice = new import_obsidian3.Notice(
          `\u6B63\u5728\u5BFC\u5165\u300C${topic.title}\u300D...`,
          0
        );
        try {
          this.settings.lastTopicId = topic.topicId;
          await this.saveSettings();
          const rows = queryNotes(db, topic.topicId);
          if (rows.length === 0) {
            importNotice.hide();
            new import_obsidian3.Notice("\u8BE5\u5B66\u4E60\u96C6\u6CA1\u6709\u7B14\u8BB0");
            db.close();
            return;
          }
          const { nodes, children, allChildren } = buildTree(rows);
          const subInfo = detectSubMindMaps(
            nodes,
            children,
            allChildren
          );
          const { rootId, orphans } = findMainRoot(
            nodes,
            children,
            allChildren,
            subInfo.roots,
            topic.title
          );
          const generated = generatePass1(
            rootId,
            orphans,
            topic.title,
            topic.topicId,
            nodes,
            children,
            subInfo.roots,
            subInfo.labels,
            subInfo.files,
            subInfo.wrappers,
            subInfo.portals,
            this.settings
          );
          const hierarchy = analyzeHierarchy(
            generated.files,
            generated.mainFile,
            subInfo.files,
            this.settings.outputFolder,
            this.settings.manualNesting,
            subInfo.labels
          );
          const finalFiles = reorganizeFiles(
            generated.files,
            subInfo.files,
            hierarchy,
            this.settings.outputFolder
          );
          let created = 0;
          let updated = 0;
          for (const [filePath, content] of finalFiles) {
            const fullPath = `${filePath}.md`;
            const existing = this.app.vault.getAbstractFileByPath(fullPath);
            if (existing) {
              await this.app.vault.modify(
                existing,
                content
              );
              updated++;
            } else {
              const dir = fullPath.substring(
                0,
                fullPath.lastIndexOf("/")
              );
              if (dir) {
                await this.ensureFolder(dir);
              }
              await this.app.vault.create(fullPath, content);
              created++;
            }
          }
          db.close();
          importNotice.hide();
          new import_obsidian3.Notice(
            `\u2705 \u5BFC\u5165\u5B8C\u6210\uFF01\u65B0\u5EFA ${created} \u4E2A\u6587\u4EF6\uFF0C\u66F4\u65B0 ${updated} \u4E2A\u6587\u4EF6`,
            5e3
          );
        } catch (err) {
          db.close();
          importNotice.hide();
          console.error("MarginNote Importer error:", err);
          new import_obsidian3.Notice(
            `\u274C \u5BFC\u5165\u5931\u8D25: ${err instanceof Error ? err.message : String(err)}`,
            8e3
          );
        }
      }).open();
    } catch (err) {
      notice.hide();
      console.error("MarginNote Importer error:", err);
      new import_obsidian3.Notice(
        `\u274C \u65E0\u6CD5\u6253\u5F00\u6570\u636E\u5E93: ${err instanceof Error ? err.message : String(err)}`,
        8e3
      );
    }
  }
  /** Recursively ensure a folder path exists in the vault */
  async ensureFolder(folderPath) {
    const parts = folderPath.split("/");
    let current = "";
    for (const part of parts) {
      current = current ? `${current}/${part}` : part;
      if (!this.app.vault.getAbstractFileByPath(current)) {
        await this.app.vault.createFolder(current);
      }
    }
  }
};
