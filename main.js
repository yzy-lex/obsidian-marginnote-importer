  serializeNesting(nesting) {
    return Object.entries(nesting).map(([child, parent]) => `${child} \u2192 ${parent}`).join("\n");
  }
  parseNesting(text) {
    const result = {};
    for (const line of text.split("\n")) {
      const trimmed = line.trim();
      if (!trimmed) continue;
      const sep = trimmed.includes("\u2192") ? "\u2192" : "->";
      const parts = trimmed.split(sep);
      if (parts.length === 2) {
        const child = parts[0].trim();
        const parent = parts[1].trim();
        if (child && parent) {
          result[child] = parent;
        }
      }
    }
    return result;
  }
